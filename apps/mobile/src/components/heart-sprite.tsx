const AnimatedHeart: React.FC<SvgProps & { bpm?: number }> = ({
  bpm = 42,
  ...props
}) => {
  const [currentFrame, setCurrentFrame] = React.useState(0);
  const frames = [
    Frame1,
    Frame2,
    Frame3,
    Frame4,
    Frame5,
    Frame6,
    Frame7,
    Frame8,
    Frame9,
    Frame10,
    Frame11,
    Frame12,
    Frame13,
    Frame14,
    Frame15,
    Frame16,
    Frame17,
    Frame18,
  ];

  React.useEffect(() => {
    let timeout: NodeJS.Timeout;

    const totalCalculatableFrames = frames.length + 1 + 8; // 8 is the number of frames that we pause after animation
    const timePerFrame = 60 / bpm;
    const timePerCycle = timePerFrame * totalCalculatableFrames;

    const advanceFrame = () => {
      setCurrentFrame((prev) => {
        const nextFrame = (prev + 1) % frames.length;
        const delay = nextFrame === 17 ? timePerCycle * 8 : timePerCycle;
        timeout = setTimeout(advanceFrame, delay);
        return nextFrame;
      });
    };

    // Start the animation
    timeout = setTimeout(advanceFrame, timePerCycle);

    // Cleanup
    return () => clearTimeout(timeout);
  }, [frames.length]);

  const FrameComponent = frames[currentFrame];

  return FrameComponent ? (
    <View className="w-100 aspect-square overflow-hidden">
      <FrameComponent
        {...props}
        width="100%"
        height="100%"
        viewBox="0 0 528 432"
        preserveAspectRatio="xMidYMid meet"
      />
    </View>
  ) : null;
};

